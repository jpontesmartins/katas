const pirates = require('./pirates');

describe('pirates', () => {
    it('should return position [4,4] for \'u\'  ', () => {

        const map = [
            ['p', '~', '~', '~', '~', '~', '~', '~'],
            ['~', '~', '~', '~', '~', '~', '~', '~'],
            ['~', '~', '~', '~', '~', '~', '~', '~'],
            ['~', '~', 'm', '~', '~', '~', '~', '~'],
            ['~', '~', '~', '~', 'u', '~', '~', '~'],
            ['~', '~', '~', '~', '~', '~', '~', '~'],
            ['~', '~', '~', '~', '~', '~', '~', '~'],
            ['~', '~', '~', '~', '~', '~', '~', '~']];

            // 'u' -> [i,j] -> [0,0] -> [4,4] = 4 + 4 = 8
            // 'm' -> [i,j] -> [0,0] -> [3,2] = 3 + 2 = 5

        const unoccupiedIsland = pirates.conquer(map);

        expect(unoccupiedIsland).toBe([[4, 4]]);
    });
});